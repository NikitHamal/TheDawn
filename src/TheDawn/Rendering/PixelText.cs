namespace TheDawn.Rendering;

public sealed class PixelText
{
    private readonly Texture2D _pixel;
    private static readonly Dictionary<char, string[]> Glyphs = BuildGlyphs();

    public PixelText(Texture2D pixel) => _pixel = pixel;

    public int MeasureWidth(string text, int scale)
    {
        var width = 0;
        foreach (var ch in text.ToUpperInvariant()) width += (ch == ' ' ? 4 : 6) * scale;
        return width;
    }

    public void Draw(SpriteBatch batch, string text, Vector2 position, Color color, int scale = 2)
    {
        var cursor = position;
        foreach (var raw in text.ToUpperInvariant())
        {
            if (raw == '\n')
            {
                cursor.X = position.X;
                cursor.Y += 9 * scale;
                continue;
            }
            if (raw == ' ')
            {
                cursor.X += 4 * scale;
                continue;
            }
            if (!Glyphs.TryGetValue(raw, out var glyph)) glyph = Glyphs['?'];
            for (var y = 0; y < glyph.Length; y++)
            {
                for (var x = 0; x < glyph[y].Length; x++)
                {
                    if (glyph[y][x] == '1')
                    {
                        batch.Draw(_pixel, new Rectangle((int)cursor.X + x * scale, (int)cursor.Y + y * scale, scale, scale), color);
                    }
                }
            }
            cursor.X += 6 * scale;
        }
    }

    public void DrawShadowed(SpriteBatch batch, string text, Vector2 position, Color color, int scale = 2)
    {
        Draw(batch, text, position + new Vector2(scale, scale), Color.Black * 0.75f, scale);
        Draw(batch, text, position, color, scale);
    }

